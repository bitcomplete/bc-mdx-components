#!/usr/bin/env node
// Build-time extractor: walks src/components/{ui/*.tsx,index.tsx},
// finds every exported component, and emits a manifest of
//   { kind: "mdx-component", name, summary, category, spec.props, examples }
// entries into dist/manifest.json + src/components/manifest.ts (so the
// IIFE bundle can ship it via globalThis.componentManifest at runtime).
//
// Convention each component must follow:
//   - JSDoc above the function declaration with:
//       summary (first paragraph)
//       @category <one of: layout|emphasis|data|conversation|agent|chronological|code-media>
//       @example (MDX usage)
//   - Inline JSDoc on each property of the props TypeLiteral (the RHS
//     of an intersection with React.ComponentProps<...>) for per-prop
//     descriptions.
//
// Drift: if the JSDoc is missing or @category is unknown, the script
// fails with a clear pointer at the offending file. There's no silent
// fallback — that's what kept the old hand-curated cheatsheet rotting.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { Project, SyntaxKind } from "ts-morph";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");

const KNOWN_CATEGORIES = new Set([
  "layout",
  "emphasis",
  "data",
  "conversation",
  "agent",
  "chronological",
  "code-media",
]);

function main() {
  const project = new Project({
    tsConfigFilePath: path.join(ROOT, "tsconfig.json"),
    skipAddingFilesFromTsConfig: false,
  });

  // The public surface: everything re-exported (or directly exported)
  // from src/components/index.tsx. Filtering to that is what keeps
  // internal primitives like ToolCallContext out of the manifest.
  const indexFile = project.getSourceFileOrThrow(
    path.join(ROOT, "src/components/index.tsx"),
  );
  const publicExports = collectPublicExports(indexFile);

  const entries = [];
  const errors = [];

  for (const { name, declaration } of publicExports) {
    if (!isComponentDeclaration(name, declaration)) continue;
    try {
      entries.push(buildEntry(name, declaration));
    } catch (err) {
      errors.push(`${name} (${shortPath(declaration.getSourceFile().getFilePath())}): ${err.message}`);
    }
  }

  if (errors.length > 0) {
    console.error("extract-manifest: failed to extract some components:\n");
    for (const e of errors) console.error("  - " + e);
    process.exit(1);
  }

  entries.sort((a, b) => a.name.localeCompare(b.name));

  const distDir = path.join(ROOT, "dist");
  fs.mkdirSync(distDir, { recursive: true });
  const manifestPath = path.join(distDir, "manifest.json");
  fs.writeFileSync(manifestPath, JSON.stringify(entries, null, 2) + "\n");

  // Also emit a TS file that the IIFE bundle can import. Keeping it
  // in src/ means tsup picks it up without a JSON loader.
  const tsPath = path.join(ROOT, "src/components/manifest.ts");
  fs.writeFileSync(
    tsPath,
    "// AUTO-GENERATED by scripts/extract-manifest.mjs. Do not edit by hand.\n" +
      "export const componentManifest = " +
      JSON.stringify(entries, null, 2) +
      " as const;\n",
  );

  console.log(
    `extract-manifest: ${entries.length} components → ${shortPath(manifestPath)}`,
  );
}

// Resolve every named export of src/components/index.tsx to its
// underlying declaration node. Handles both `export { X } from "..."`
// re-exports and inline `export function X(...)` forms.
function collectPublicExports(indexFile) {
  const out = [];
  for (const [name, declarations] of indexFile.getExportedDeclarations()) {
    const decl = declarations[0];
    if (!decl) continue;
    out.push({ name, declaration: decl });
  }
  return out;
}

// Names that ARE exported from index.tsx (so technically part of the
// public package API) but aren't usable directly from MDX — they're
// internal building blocks for our MDX-facing wrappers. Documenting
// them in the manifest would only confuse agents.
const INTERNAL_NAMES = new Set([
  "ToolCallPrimitive",
  "ToolCallLabel",
  "ToolCallContent",
  "ToolTreePrimitive",
  "ToolTreeTrigger",
  "ToolTreeContent",
]);

function isComponentDeclaration(name, node) {
  // We treat as a component anything that's a FunctionDeclaration whose
  // name starts with a capital letter and isn't on the internal list.
  if (!node) return false;
  if (node.getKind() !== SyntaxKind.FunctionDeclaration) return false;
  const fnName = node.getName?.();
  if (!fnName || !/^[A-Z]/.test(fnName)) return false;
  if (INTERNAL_NAMES.has(name)) return false;
  return true;
}

function buildEntry(name, fn) {
  const jsDocs = fn.getJsDocs?.() ?? [];
  if (jsDocs.length === 0) {
    throw new Error(
      "missing JSDoc. Add `/** … @category … @example … */` above the function.",
    );
  }
  const jsdoc = jsDocs[jsDocs.length - 1];

  const description = jsdoc.getDescription().trim();
  if (!description) {
    throw new Error("JSDoc must have a summary sentence before any @tag.");
  }
  const summary = firstParagraph(description);

  const category = readTagText(jsdoc, "category");
  if (!category) {
    throw new Error("missing @category tag.");
  }
  if (!KNOWN_CATEGORIES.has(category)) {
    throw new Error(
      `unknown @category "${category}". Valid: ${[...KNOWN_CATEGORIES].join(", ")}.`,
    );
  }

  const examples = jsdoc
    .getTags()
    .filter((t) => t.getTagName() === "example")
    .map((t) => trimExample(t.getCommentText() ?? ""))
    .filter(Boolean);

  const props = extractProps(fn);

  return {
    kind: "mdx-component",
    name,
    summary,
    category,
    spec: { props },
    examples,
  };
}

function firstParagraph(text) {
  const idx = text.indexOf("\n\n");
  const para = (idx === -1 ? text : text.slice(0, idx)).trim();
  // Collapse hard-wraps inside the paragraph so the summary fits on
  // one line in the CLI output.
  return para.replace(/\s*\n\s*/g, " ");
}

function readTagText(jsdoc, tagName) {
  const tag = jsdoc.getTags().find((t) => t.getTagName() === tagName);
  if (!tag) return null;
  return (tag.getCommentText() ?? "").trim();
}

function trimExample(s) {
  // JSDoc strips leading "* " but leaves indentation; normalise it.
  const lines = s.split("\n");
  const min = Math.min(
    ...lines.filter((l) => l.trim()).map((l) => l.match(/^\s*/)[0].length),
  );
  return lines.map((l) => l.slice(min)).join("\n").trim();
}

// Pull props out of the function's first parameter type. We only
// document the inline TypeLiteral, which is the RHS of an intersection
// like `React.ComponentProps<"div"> & { foo?: T }`. Anything inherited
// from the HTML attributes side is omitted — it's noise for the
// agent-facing docs.
function extractProps(fn) {
  const params = fn.getParameters();
  if (params.length === 0) return [];
  const typeNode = params[0].getTypeNode();
  if (!typeNode) return [];

  // Collect defaults from the destructuring pattern.
  const defaults = readDestructuringDefaults(params[0]);

  // Find the TypeLiteral somewhere in the type. Common shapes we handle:
  //   { ... }                     — bare TypeLiteral
  //   React.ComponentProps<"x"> & { ... }  — intersection
  //   React.ComponentProps<"x">   — no inline props
  const literals = findInlineTypeLiterals(typeNode);
  if (literals.length === 0) return [];

  const props = [];
  for (const literal of literals) {
    for (const member of literal.getProperties()) {
      const propName = member.getName();
      const optional = !!member.hasQuestionToken?.();
      // Resolve to expanded text. For type aliases (CalloutType) and
      // unions we want the literal text — "info" | "warning" | …
      // rather than just "CalloutType". Drop the implicit `| undefined`
      // that optional props pick up.
      let propType = expandedTypeText(member);
      if (optional) {
        propType = propType
          .replace(/\s*\|\s*undefined\s*$/, "")
          .replace(/^undefined\s*\|\s*/, "");
      }
      const descTag = member.getJsDocs?.()[0];
      const description = descTag
        ? descTag.getDescription().trim() || (descTag.getCommentText?.() ?? "")
        : "";
      const def = defaults[propName];
      props.push({
        name: propName,
        type: cleanType(propType),
        optional,
        ...(def !== undefined ? { default: def } : {}),
        ...(description ? { description } : {}),
      });
    }
  }
  return props;
}

function findInlineTypeLiterals(typeNode) {
  // Walk the type expression looking for `{ … }` literals. For
  // intersections we keep all literals; for everything else we keep
  // none unless the node itself is a literal.
  const out = [];
  function visit(n) {
    const k = n.getKind();
    if (k === SyntaxKind.TypeLiteral) {
      out.push(n);
      return;
    }
    if (k === SyntaxKind.IntersectionType) {
      for (const t of n.getTypeNodes()) visit(t);
      return;
    }
    if (k === SyntaxKind.ParenthesizedType) {
      visit(n.getTypeNode());
      return;
    }
    // TypeReferences (e.g. React.ComponentProps<"div">), unions,
    // and bare identifiers don't have inline literals we care about.
  }
  visit(typeNode);
  return out;
}

function readDestructuringDefaults(param) {
  const out = {};
  const name = param.getNameNode();
  if (name.getKind() !== SyntaxKind.ObjectBindingPattern) return out;
  for (const el of name.getElements()) {
    const init = el.getInitializer?.();
    if (!init) continue;
    out[el.getName()] = init.getText();
  }
  return out;
}

// ts-morph's Type#getText() returns the alias name when one exists
// (so `CalloutType` instead of `"info" | "warning" | ...`). Agents
// reading `pin components` need the literal union, since they can't
// look up the alias. But expanding everything turns `boolean` into
// `false | true` and `React.ReactNode` into a 12-arm monster. So:
// expand only when the result is shorter than the unexpanded form,
// or when the alias name is one we know is opaque ("CalloutType",
// "KPITone", etc — anything ending in "Type", "Tone", "Status",
// "Severity", "State", "Color"). Otherwise keep the alias.
function expandedTypeText(node) {
  const t = node.getType();
  const raw = cleanType(t.getText(node));

  if (!t.isUnion()) return raw;

  const expanded = t
    .getUnionTypes()
    .map((u) => cleanType(u.getText(node)))
    .join(" | ");

  // Bare `boolean` shouldn't expand to `false | true`.
  if (/^(boolean|boolean \| undefined|undefined \| boolean)$/.test(raw)) {
    return raw;
  }

  // Expand if the original is an opaque alias OR if the expanded
  // form is meaningfully more useful (literal union of strings).
  const isOpaqueAlias = /\b[A-Z]\w*(Type|Tone|Status|Severity|State|Color)\b/.test(raw);
  const expandedIsAllLiterals = t
    .getUnionTypes()
    .every((u) => u.isLiteral() || u.isUndefined());

  if (isOpaqueAlias || (expandedIsAllLiterals && expanded.length < 120)) {
    return expanded;
  }
  return raw;
}

function cleanType(s) {
  // ts-morph returns the resolved type which may include nested
  // module imports like `import("…/react").ReactNode`. Strip those for
  // readability without changing meaning.
  return s.replace(/import\("[^"]+"\)\./g, "").trim();
}

function shortPath(p) {
  return path.relative(ROOT, p);
}

main();
